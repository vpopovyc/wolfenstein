#include "libft.h"

void	ft_ppdel(char ***sv)
{
	char	i;

	i = 0;
	if (*sv != NULL && **sv != NULL)
	{
		while (**sv)
		{
			free(**sv);
			**sv = NULL;
			++(*sv);
			++i;
		}
		*sv -= i;
		free(*sv);
		*sv = NULL;
	}
}